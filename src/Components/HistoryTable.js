const EnhancedTableToolbar = (props) => {
    const classes = useToolbarStyles();
    const [openTnxModal, setOpenTnxModal] = React.useState(false);
    return (
        <Toolbar
            className={clsx(classes.root)}
        >
            <Typography className={classes.title} variant="h6" id="tableTitle" component="div">
                History
            </Typography>
            <Button style={{ marginRight: 10 }} onClick={() => setOpenTnxModal(true)}>
                Transaction
            </Button>
            <AddTnxModal open={openTnxModal} setOpen={setOpenTnxModal} />
        </Toolbar>
    );
};
export default function HistoryTable() {
    const { userData } = useContext(AuthContext);
    const classes = useStyles();
    const [order, setOrder] = React.useState('desc');
    const [orderBy, setOrderBy] = React.useState('date');
    const [page, setPage] = React.useState(0);
    const [rowsPerPage, setRowsPerPage] = React.useState(5);
    const [rows, setRows] = React.useState([]);

    React.useEffect(() => {
        setRows(userData?.transaction || []);
    }, [userData])
    const handleRequestSort = (event, property) => {
        const isAsc = orderBy === property && order === 'asc';
        setOrder(isAsc ? 'desc' : 'asc');
        setOrderBy(property);
    };

    const handleChangePage = (event, newPage) => {
        setPage(newPage);
    };

    const handleChangeRowsPerPage = (event) => {
        setRowsPerPage(parseInt(event.target.value, 10));
        setPage(0);
    };

    return (
        <div className={classes.root}>
            <Paper className={classes.paper}>
                <EnhancedTableToolbar />
                <TableContainer>
                    <Table
                        className={classes.table}
                        aria-labelledby="tableTitle"
                        size={'medium'}
                        aria-label="enhanced table"
                    >
                        <EnhancedTableHead
                            classes={classes}
                            order={order}
                            orderBy={orderBy}
                            onRequestSort={handleRequestSort}
                            rowCount={rows?.length || 0}
                        />
                        <TableBody>
                            {stableSort(rows, getComparator(order, orderBy))
                                .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
                                .map((row, index) => {

                                    return (
                                        <TableRow
                                            hover
                                            tabIndex={-1}
                                            key={row?.date + row?.amount + row.type +index}
                                        >
                                            <TableCell>{moment(new Date(row.date)).format('DD MMM YY')}</TableCell>
                                            <TableCell>{row.type}</TableCell>
                                            <TableCell >{(row.amount).toLocaleString('en-IN')}</TableCell>
                                        </TableRow>
                                    );
                                })}
                            <TableRow>
                                <TableCell rowSpan={3} />
                                <TableCell colSpan={1}>Total Capital</TableCell>
                                <TableCell style={{fontSize:20}} align="right">{(TotalCapital(rows)).toLocaleString('en-IN')}</TableCell>
                            </TableRow>
                            {/* {emptyRows > 0 && (
                                <TableRow style={{ height: (53) * emptyRows }}>
                                    <TableCell colSpan={6} />
                                </TableRow>
                            )} */}
                        </TableBody>
                    </Table>
                </TableContainer>
                <TablePagination
                    rowsPerPageOptions={[5, 10, 25]}
                    component="div"
                    count={rows?.length || 0}
                    rowsPerPage={rowsPerPage}
                    page={page}
                    onPageChange={handleChangePage}
                    onRowsPerPageChange={handleChangeRowsPerPage}
                />
            </Paper>
        </div>
    );
}
